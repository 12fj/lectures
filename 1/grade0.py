score = int(input("Score: "))

if 90 <= score and score <= 100:
    print("Grade: A")
if 80 <= score and score < 90:
    print("Grade: B")
if 70 <= score and score < 80:
    print("Grade: C")
if 60 <= score and score < 70:
    print("Grade: D")
if 0 <= score and score < 60:
    print("Grade: F")
